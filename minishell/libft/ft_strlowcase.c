#include "libft.h"

char	*ft_strlowcase(char *s)
{
	size_t	i;

	if (!s)
		return (NULL);
	i = 0;
	while (s[i])
	{
		if (ft_isupper(s[i]))
			s[i] += 32;
		i++;
	}
	return (s);
}
