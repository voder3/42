#include "libft.h"

long long	ft_atoll(const char *str)
{
	long long	i;
	long long	sum;
	int			neg;

	i = 0;
	sum = 0;
	neg = 1;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			neg = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		sum = sum * 10 + str[i] - 48;
		i++;
	}
	return (sum * neg);
}
